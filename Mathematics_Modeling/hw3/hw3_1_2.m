% 验证维度
disp('x 的长度：'); disp(length(x)); % 应为 15
disp('y 的长度：'); disp(length(y)); % 应为 13
disp('z 的维度：'); disp(size(z)); % 应为 13×15

% 步骤 2：生成插值网格
% 在 x 和 y 方向分别插值 50 点
xi = 0:50:5600;yi=4800:-50:0;


% 步骤 3：使用 csape 进行二维样条插值
% 构造二维三次样条
pp = csape({y, x}, z, 'not-a-knot'); % 构造样条，使用 not-a-knot 边界条件

% 在插值网格上求值
% csape 返回的样条需要使用 fnval 求值
[Xi, Yi] = meshgrid(xi, yi); % 生成网格
Zi = fnval(pp, {yi, xi}); % 求值，注意输入顺序与 csape 的定义一致

% 步骤 4：绘制等高线图
figure;
contourf(Xi, Yi, Zi, 20); % 绘制填充等高线图，20 个等高线级别
colorbar; % 添加颜色条
xlabel('x (m)');
ylabel('y (m)');
title('高程分布等高线图 (m) - 使用 csape 插值');
grid on;

% 步骤 5：绘制三维表面图
figure;
surf(Xi, Yi, Zi, 'EdgeColor', 'none'); % 绘制三维表面图
colorbar;
xlabel('x (m)');
ylabel('y (m)');
zlabel('高程 (m)');
title('高程分布三维图 - 使用 csape 插值');