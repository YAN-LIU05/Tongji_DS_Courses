% 验证维度
disp('x 的长度：'); disp(length(x)); % 应为 15
disp('y 的长度：'); disp(length(y)); % 应为 13
disp('z 的维度：'); disp(size(z)); % 应为 13×15c

% 步骤 2：生成插值网格
% 在 x 和 y 方向分别插值 50 点
xi = linspace(0, 5600, 50); % x 方向 50 个点
yi = linspace(0, 4800, 50); % y 方向 50 个点
[Xi, Yi] = meshgrid(xi, yi); % 生成网格

% 步骤 3：二维插值
Zi = interp2(x, y, z, Xi, Yi, 'cubic'); % 使用三次插值方法，得到更平滑的结果

% 步骤 4：绘制等高线图
figure;
contourf(Xi, Yi, Zi, 20); % 绘制填充等高线图，20 个等高线级别
colorbar; % 添加颜色条
xlabel('x (m)');
ylabel('y (m)');
title('高程分布等高线图 (m) - 使用三次插值');
grid on;

% 步骤 5：可选 - 绘制三维表面图（便于观察地形）
figure;
surf(Xi, Yi, Zi, 'EdgeColor', 'none'); % 绘制三维表面图
colorbar;
xlabel('x (m)');
ylabel('y (m)');
zlabel('高程 (m)');
title('高程分布三维图 - 使用三次插值');